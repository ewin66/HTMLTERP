        #region 新方法
        public void RSave(BaseEntity[] p_BE, int p_ID)
        {
            try
            {
                IDBTransAccess sqlTrans = TransSysUtils.GetDBTransAccess();
                try
                {
                    sqlTrans.OpenTrans();

                    this.RSave(p_BE, p_ID, sqlTrans);

                    sqlTrans.CommitTrans();
                }
                catch (Exception TE)
                {
                    sqlTrans.RollbackTrans();
                    throw TE;
                }
            }
            catch (BaseException)
            {
                throw;
            }
            catch (Exception E)
            {
                throw new BaseException(E.Message);
            }
        }


        public void RSave(BaseEntity[] p_BE, int p_ID, IDBTransAccess sqlTrans)
        {
            try
            {
                string sql = "DELETE Data_VendorFile WHERE MainID=" + SysString.ToDBString(p_ID);
                sqlTrans.ExecuteNonQuery(sql);
                for (int i = 0; i < p_BE.Length; i++)
                {
                    VendorFileRule rule = new VendorFileRule();
                    VendorFile entity = (VendorFile)p_BE[i];
                    entity.MainID = p_ID;
                    entity.Seq = i + 1;
                    rule.RAdd(entity, sqlTrans);

                }
            }
            catch (BaseException)
            {
                throw;
            }
            catch (Exception E)
            {
                throw new BaseException(E.Message);
            }
        }


