using System;
using System.Data;
using System.Data.SqlClient;
using System.Text;
using System.Collections;
using HttSoft.Framework;
using HttSoft.MLTERP.Data;
using HttSoft.MLTERP.Sys;

namespace HttSoft.MLTERP.DataCtl
{
	/// <summary>
	/// 目的：Enum_WHType实体控制类
	/// 作者:刘德苏
	/// 创建日期:2012/4/23
	/// </summary>
	public sealed class WHTypeCtl : BaseControl
	{
	    private bool sqlTransFlag=false;
		private IDBTransAccess sqlTrans;
		
	    /// <summary>
        /// 构造函数
        /// </summary>
        public WHTypeCtl()
		{
		    
		}
		
		/// <summary>
        /// 构造函数
        /// </summary>
        public WHTypeCtl(IDBTransAccess p_SqlCmd)
		{
		    sqlTrans=p_SqlCmd;
			sqlTransFlag=true;
		}
		
		/// <summary>
        /// 新增
        /// </summary>
        /// <param name="p_Entity">实体类</param>
        /// <returns>操作影响的记录行数</returns>
        public override int AddNew(BaseEntity p_Entity)
        {
            try
            {
                WHType MasterEntity=(WHType)p_Entity;
                if (MasterEntity.ID==0)
                {
                    return 0;
                }

                //新增主表数据
                StringBuilder MasterField=new StringBuilder();
                StringBuilder MasterValue=new StringBuilder();
                MasterField.Append("INSERT INTO Enum_WHType(");
                MasterValue.Append(" VALUES(");
                MasterField.Append("ID"+","); 
  				MasterValue.Append(SysString.ToDBString(MasterEntity.ID)+","); 
  				MasterField.Append("Code"+","); 
  				MasterValue.Append(SysString.ToDBString(MasterEntity.Code)+","); 
  				MasterField.Append("Name"+","); 
  				MasterValue.Append(SysString.ToDBString(MasterEntity.Name)+","); 
  				MasterField.Append("WHPosMethodID"+","); 
  				MasterValue.Append(SysString.ToDBString(MasterEntity.WHPosMethodID)+","); 
  				MasterField.Append("ItemTypeID"+","); 
  				MasterValue.Append(SysString.ToDBString(MasterEntity.ItemTypeID)+","); 
  				MasterField.Append("Remark"+","); 
  				MasterValue.Append(SysString.ToDBString(MasterEntity.Remark)+","); 
  				MasterField.Append("Tax"+","); 
  				if(MasterEntity.Tax!=0) 
  				{ 
  			 		MasterValue.Append(SysString.ToDBString(MasterEntity.Tax)+","); 
  				} 
  				else 
  				{ 
  			 		MasterValue.Append("null,"); 
  				} 
  
  				MasterField.Append("ItemTypeID2"+","); 
  				MasterValue.Append(SysString.ToDBString(MasterEntity.ItemTypeID2)+","); 
  				MasterField.Append("ItemTypeID3"+","); 
  				MasterValue.Append(SysString.ToDBString(MasterEntity.ItemTypeID3)+","); 
  				MasterField.Append("ItemTypeID4"+","); 
  				MasterValue.Append(SysString.ToDBString(MasterEntity.ItemTypeID4)+","); 
  				MasterField.Append("ItemTypeID5"+")"); 
  				MasterValue.Append(SysString.ToDBString(MasterEntity.ItemTypeID5)+")"); 
 
                
                

                //执行
                int AffectedRows=0;
				if(!this.sqlTransFlag)
				{
					AffectedRows=this.ExecuteNonQuery(MasterField.Append(MasterValue.ToString()).ToString());
				}
				else
				{
					AffectedRows=sqlTrans.ExecuteNonQuery(MasterField.Append(MasterValue.ToString()).ToString());
				}
                return AffectedRows;
            }
            catch(BaseException E)
            {
                throw new BaseException(E.Message,E);
            }
            catch(Exception E)
            {
               throw new BaseException(FrameWorkMessage.GetAlertMessage((int)Message.CommonDBInsert),E);
            }
        }

        /// <summary>
        /// 修改
        /// </summary>
        /// <param name="p_Entity">实体类</param>
        /// <returns>操作影响的记录行数</returns>
        public override int Update(BaseEntity p_Entity)
        {
            try
            {
                WHType MasterEntity=(WHType)p_Entity;
                if (MasterEntity.ID==0)
                {
                    return 0;
                }

                //更新主表数据
                StringBuilder UpdateBuilder=new StringBuilder();
                UpdateBuilder.Append("UPDATE Enum_WHType SET ");
                UpdateBuilder.Append(" ID="+SysString.ToDBString(MasterEntity.ID)+","); 
  				UpdateBuilder.Append(" Code="+SysString.ToDBString(MasterEntity.Code)+","); 
  				UpdateBuilder.Append(" Name="+SysString.ToDBString(MasterEntity.Name)+","); 
  				UpdateBuilder.Append(" WHPosMethodID="+SysString.ToDBString(MasterEntity.WHPosMethodID)+","); 
  				UpdateBuilder.Append(" ItemTypeID="+SysString.ToDBString(MasterEntity.ItemTypeID)+","); 
  				UpdateBuilder.Append(" Remark="+SysString.ToDBString(MasterEntity.Remark)+","); 
  				 
  				if(MasterEntity.Tax!=0) 
  				{ 
  			 		UpdateBuilder.Append(" Tax="+SysString.ToDBString(MasterEntity.Tax)+","); 
  				} 
  				else 
  				{ 
  			 		UpdateBuilder.Append(" Tax=null,");  
  				} 
  
  				UpdateBuilder.Append(" ItemTypeID2="+SysString.ToDBString(MasterEntity.ItemTypeID2)+","); 
  				UpdateBuilder.Append(" ItemTypeID3="+SysString.ToDBString(MasterEntity.ItemTypeID3)+","); 
  				UpdateBuilder.Append(" ItemTypeID4="+SysString.ToDBString(MasterEntity.ItemTypeID4)+","); 
  				UpdateBuilder.Append(" ItemTypeID5="+SysString.ToDBString(MasterEntity.ItemTypeID5)); 
 
                UpdateBuilder.Append(" WHERE "+ "ID="+SysString.ToDBString(MasterEntity.ID));
                
                

               //执行
				int AffectedRows=0;
				if(!this.sqlTransFlag)
				{
					AffectedRows=this.ExecuteNonQuery(UpdateBuilder.ToString());
				}
				else
				{
					AffectedRows=sqlTrans.ExecuteNonQuery(UpdateBuilder.ToString());
				}
                return AffectedRows;
            }
            catch(BaseException E)
            {
                throw new BaseException(E.Message,E);
            }
            catch(Exception E)
            {
                throw new BaseException(FrameWorkMessage.GetAlertMessage((int)Message.CommonDBUpdate),E);
            }
        }

        /// <summary>
        /// 删除
        /// </summary>
        /// <param name="p_Entity">实体类</param>
        /// <returns>操作影响的记录行数</returns>
        public override int Delete(BaseEntity p_Entity)
        {
            try
            {
                WHType MasterEntity=(WHType)p_Entity;
                if (MasterEntity.ID==0)
                {
                    return 0;
                }

                //删除主表数据
                string Sql="";
                Sql="DELETE FROM Enum_WHType WHERE "+ "ID="+SysString.ToDBString(MasterEntity.ID);
                //执行
				int AffectedRows=0;
				if(!this.sqlTransFlag)
				{
					AffectedRows=this.ExecuteNonQuery(Sql);
				}
				else
				{
					AffectedRows=sqlTrans.ExecuteNonQuery(Sql);
				}

                return AffectedRows;
            }
            catch(BaseException E)
            {
                throw new BaseException(E.Message,E);
            }
            catch(Exception E)
            {
                throw new BaseException(FrameWorkMessage.GetAlertMessage((int)Message.CommonDBDelete),E);
            }
        }
	}
}
