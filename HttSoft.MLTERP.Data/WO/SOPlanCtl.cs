        {
            try
            {
                SOPlan MasterEntity=(SOPlan)p_Entity;
                if (MasterEntity.ID==0)
                {
                    return 0;
                }

                //删除主表数据
                string Sql="";
                Sql="DELETE FROM WO_SOPlan WHERE "+ "ID="+SysString.ToDBString(MasterEntity.ID);
